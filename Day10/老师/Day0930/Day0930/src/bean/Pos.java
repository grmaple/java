package bean;

public class Pos
{
	/*
	 * 职位信息类
	 */
	private int id;
	private String name;
	private int depid;
	public int getId()
	{
		return id;
	}
	public void setId(int id)
	{
		this.id = id;
	}
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	public int getDepid()
	{
		return depid;
	}
	public void setDepid(int depid)
	{
		this.depid = depid;
	}
}
